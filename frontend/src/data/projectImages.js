export const isVideo = (path) => /\.(mp4|webm|mov)$/i.test(path);

// Encoded, ready-to-use media descriptors for a project's imageFolder.
export const getProjectMedia = (imageFolder) =>
  (PROJECT_IMAGES[imageFolder] || []).map((src) => ({
    src: encodeURI(src),
    video: isVideo(src),
  }));

// First image of a project, encoded — used for grid thumbnails.
export const getProjectCover = (imageFolder) => {
  const list = PROJECT_IMAGES[imageFolder] || [];
  const cover = list.find((p) => !isVideo(p)) || list[0];
  return cover ? encodeURI(cover) : null;
};
